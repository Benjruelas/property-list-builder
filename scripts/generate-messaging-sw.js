#!/usr/bin/env node
/**
 * Generates public/firebase-messaging-sw.js from environment variables.
 * Run before build: node scripts/generate-messaging-sw.js
 * Reads from .env.local or process.env (Vercel injects at build).
 */

import { readFileSync, writeFileSync, existsSync } from 'fs'
import { fileURLToPath } from 'url'
import { dirname, join } from 'path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')

// Load .env.local if it exists
const envPath = join(root, '.env.local')
if (existsSync(envPath)) {
  const content = readFileSync(envPath, 'utf-8')
  for (const line of content.split('\n')) {
    const m = line.match(/^([^#=]+)=(.*)$/)
    if (m) {
      const key = m[1].trim()
      const val = m[2].trim().replace(/^["']|["']$/g, '')
      if (!(key in process.env)) process.env[key] = val
    }
  }
}

const config = {
  apiKey: process.env.VITE_FIREBASE_API_KEY || 'YOUR_API_KEY',
  authDomain: process.env.VITE_FIREBASE_AUTH_DOMAIN || 'localhost',
  projectId: process.env.VITE_FIREBASE_PROJECT_ID || 'YOUR_PROJECT_ID',
  storageBucket: process.env.VITE_FIREBASE_STORAGE_BUCKET || '',
  messagingSenderId: process.env.VITE_FIREBASE_MESSAGING_SENDER_ID || '',
  appId: process.env.VITE_FIREBASE_APP_ID || 'YOUR_APP_ID'
}

const swContent = `/* eslint-disable */
/* Generated by scripts/generate-messaging-sw.js - do not edit manually */
importScripts('https://www.gstatic.com/firebasejs/10.7.0/firebase-app-compat.js');
importScripts('https://www.gstatic.com/firebasejs/10.7.0/firebase-messaging-compat.js');

firebase.initializeApp({
  apiKey: "${config.apiKey}",
  authDomain: "${config.authDomain}",
  projectId: "${config.projectId}",
  storageBucket: "${config.storageBucket}",
  messagingSenderId: "${config.messagingSenderId}",
  appId: "${config.appId}"
});

const messaging = firebase.messaging();

messaging.onBackgroundMessage((payload) => {
  const { title, body, icon } = payload.data || {};
  const options = {
    body: body || payload.notification?.body || 'New notification',
    icon: icon || '/icon-192.svg',
    badge: '/icon-192.svg',
    tag: payload.data?.tag || 'property-list-builder',
    data: payload.data || {}
  };
  self.registration.showNotification(title || payload.notification?.title || 'Property List Builder', options);
});
`

const outPath = join(root, 'public', 'firebase-messaging-sw.js')
writeFileSync(outPath, swContent, 'utf-8')
console.log('Generated public/firebase-messaging-sw.js')
